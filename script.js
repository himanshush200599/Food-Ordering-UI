function init(n,res){
  index  = n*restraPerPage;
  prev = index-restraPerPage;
  temp = res.slice(prev,index);
  let text = '';
  temp.forEach((item)=>{ 
    text+=display(item);
  })
  
  container.innerHTML = text;
}
